import { Snowball } from "components";
import { Button } from "components/Button";
import Header from "components/Header";
import { useSnowball } from "hooks/queries/useSnowball";
import Head from "next/head";
import Image from "next/image";
import { useRouter } from "next/router";
import { useRecoilValue, useSetRecoilState } from "recoil";
import { getSnowball } from "service/api";
import { authState, redirectUrlState, userState } from "stores";

import * as S from "styles/home";

export default function Home() {
  const router = useRouter();
  const user = useRecoilValue(userState);
  const auth = useRecoilValue(authState);
  const setRedirect = useSetRecoilState(redirectUrlState);
  const { hasSnowball } = useSnowball();

  const handlePrimaryButtonClick = async () => {
    if (hasSnowball) {
      router.push(`/snowball/${user.id}`);
      return;
    }
    if (auth.accessToken) {
      router.push("/create");
      return;
    }
    setRedirect("/create");
    router.push("/login");
  };

  return (
    <>
      <Head>
        <title>My Snowball</title>
        <meta
          name="친구의 스노우볼을 채워주세요. "
          content="Generated by My Snowball"
        />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header />
      <S.Layout>
        <S.SnowballLayout>
          <S.Title>
            <Image
              src="/images/title.png"
              alt="제목"
              objectFit="contain"
              layout="fill"
              priority
            ></Image>
          </S.Title>
          <Snowball />
        </S.SnowballLayout>
      </S.Layout>
      <S.Footer>
        {hasSnowball ? (
          <Button onClick={handlePrimaryButtonClick}>내 스노우볼 보기</Button>
        ) : (
          <Button onClick={handlePrimaryButtonClick}>내 스노우볼 만들기</Button>
        )}
      </S.Footer>
    </>
  );
}
